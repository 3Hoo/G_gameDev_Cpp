void TileMapComponent::Draw(SDL_Renderer* renderer)
{
	SDL_Rect srsrect;
	SDL_Rect dstrect;

	srsrect.w = 32;
	srsrect.h = 32;
	
	dstrect.w = 32;
	dstrect.h = 32;

	if (GetTexture())
	{
		for (int i = 0; i < 24; i++)
		{
			for (int j = 0; j < 32; j++)
			{
				if (mTileMap[i][j] == -1)
				{
					SDL_Log("draw (row : %d, col : %d) = %d", i, j, mTileMap[i][j]);
					break;
				}
				else
				{
					srsrect.x = (mTileMap[i][j] % 8) * 32;
					srsrect.y = (mTileMap[i][j] / 8) * 32;
					dstrect.x = i * 32;
					dstrect.y = j * 32;

					SDL_RenderCopyEx(renderer,
						GetTexture(),
						&dstrect,
						&srsrect,
						-Math::ToDegrees(mOwner->GetRotation()),
						nullptr,
						SDL_FLIP_NONE
					);
					SDL_Log("draw (row : %d, col : %d) = %d", i, j, mTileMap[i][j]);
				}

			}
		}
	}
}